#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	c;

	if (n < 0)
		write(fd, "-", 1);
	if (n <= -10 || n >= 10)
		ft_putnbr_fd((n / 10) * ((n > 0) * 2 - 1), fd);
	c = (n % 10) * ((n > 0) * 2 - 1) + '0';
	write(fd, &c, 1);
}
